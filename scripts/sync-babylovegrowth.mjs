async function apiGet(pathname) {
  for (let attempt = 0; attempt < MAX_API_ATTEMPTS; attempt += 1) {
    const response = await fetch(`${API_BASE}${pathname}`, {
      headers: {
        "X-API-Key": apiKey,
        "Content-Type": "application/json",
      },
    });

    if (response.ok) return response.json();

    if (response.status === 429 && attempt < MAX_API_ATTEMPTS - 1) {
      const delay = retryDelay(response, attempt);
      console.warn(`BabyLoveGrowth API rate limited ${pathname}; retrying in ${delay}ms.`);
      await sleep(delay);
      continue;
    }

    throw new Error(`BabyLoveGrowth API ${response.status} for ${pathname}`);
  }

  throw new Error(`BabyLoveGrowth API retry limit exceeded for ${pathname}`);
}

async function fetchArticles() {
  const summaries = await apiGet(`/v1/articles?limit=${limit}&offset=0`);
  const articles = [];
  for (const summary of summaries) {
    if (articles.length > 0) await sleep(DETAIL_DELAY_MS);
    articles.push(await apiGet(`/v1/articles/${summary.id}`));
  }
  return articles;
}

async function upsertGeneratedBlock(filePath, startMarker, endMarker, content) {
  const original = await readFile(filePath, "utf8");
  if (!original.includes(startMarker) || !original.includes(endMarker)) {
    throw new Error(`Missing generated block markers in ${filePath}`);
  }
  const next = original.replace(
    new RegExp(`${startMarker}[\\s\\S]*?${endMarker}`),
    `${startMarker}\n${content.trim()}\n${endMarker}`
  );
  if (next !== original && !dryRun) await writeFile(filePath, next);
}

async function main() {
  const template = await readFile(TEMPLATE_PATH, "utf8");
  const templateParts = extractTemplateParts(template);
  const articles = await fetchArticles();
  const synced = [];

  for (const article of articles) {
    const slug = slugify(article.slug || article.title || String(article.id));
    const dir = path.join(RESOURCE_ROOT, slug);
    const filePath = path.join(dir, "index.html");
    if (existsSync(filePath)) {
      const existing = await readFile(filePath, "utf8");
      if (!existing.includes(`babylovegrowth:article-id=${article.id}`)) {
        console.warn(`Skipping ${slug}: existing non-BabyLoveGrowth page found.`);
        continue;
      }
    }
    const page = buildPage(article, templateParts);
    if (!dryRun) {
      await mkdir(dir, { recursive: true });
      await writeFile(filePath, page);
    }
    synced.push({
      id: article.id,
      title: article.title,
      slug,
      description: article.meta_description || article.excerpt || "",
      date: formatDate(article.created_at),
    });
  }

  const cards = synced
    .map(
      (article) => `      <article class="resource-card">
        <span class="chip">BabyLoveGrowth</span>
        <h2><a href="/resources/${article.slug}/">${escapeHtml(article.title)}</a></h2>
        <p>${escapeHtml(article.description)}</p>
        <a class="resource-card__link" href="/resources/${article.slug}/">Read article &rarr;</a>
      </article>`
    )
    .join("\n");

  const sitemapUrls = synced
    .map(
      (article) => `  <url>
    <loc>${SITE_URL}/resources/${article.slug}/</loc>
    <lastmod>${article.date}</lastmod>
    <changefreq>monthly</changefreq>
    <priority>0.5</priority>
  </url>`
    )
    .join("\n");

  const llmsItems = synced
    .map((article) => `- ${article.title}: ${SITE_URL}/resources/${article.slug}/`)
    .join("\n");

  await upsertGeneratedBlock(
    path.join(RESOURCE_ROOT, "index.html"),
    "<!-- BABYLOVEGROWTH_RESOURCES_START -->",
    "<!-- BABYLOVEGROWTH_RESOURCES_END -->",
    cards
  );
  await upsertGeneratedBlock(
    path.join(ROOT, "sitemap.xml"),
    "<!-- BABYLOVEGROWTH_SITEMAP_START -->",
    "<!-- BABYLOVEGROWTH_SITEMAP_END -->",
    sitemapUrls
  );
  await upsertGeneratedBlock(
    path.join(ROOT, "llms.txt"),
    "<!-- BABYLOVEGROWTH_LLMS_START -->",
    "<!-- BABYLOVEGROWTH_LLMS_END -->",
    llmsItems
  );

  console.log(`Synced ${synced.length} BabyLoveGrowth article(s).`);
}

main().catch((error) => {
  console.error(error.message);
  process.exit(1);
});
